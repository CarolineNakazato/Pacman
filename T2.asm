TITLE PACMAN
.MODEL SMALL
.STACK 100h
.DATA
    matriz db 00h,00h,00h
           db 00h,00h,00h
           db 00h,00h,00h
;------------------------------------------------------------------------------------------------------------------
.CODE
PROC MAIN
        mov     AX,@DATA
        mov     DS,AX
        mov     AH,04Ch
        int     021h
ENDP MAIN

;;===========================================================
;;================  Teclado  ================================
PROC Teclado
teclado:
        mov     AH,00h
        int     016h
        cmp     AH,048h
        je      up
        cmp     AH,04Bh
        je      left
        cmp     AH,04Dh
        je      right
        cmp     AH,050h
        je      down
        cmp     AH,01h
        jne     teclado
        jmp     teclado_fim

up:
        jmp     teclado
left:
        jmp     teclado
right:
        jmp     teclado
down:
        jmp     teclado

teclado_fim:
        ret
Teclado ENDP
